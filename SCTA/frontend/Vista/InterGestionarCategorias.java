    /**
     * Consulta la base de datos y llena el DefaultTableModel con las
     * categorías. Configura la tabla para que las celdas no sean editables
     * directamente.
     */
    public void cargarTablaCategoria() {
        DefaultTableModel model = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // Bloquea la edición directa en celdas
            }
        };

        model.addColumn("ID");
        model.addColumn("Descripción");

        try {
            Connection con = Conexión.conectar();
            String sql = "select id_categoria, descripcion from categoria";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                Object fila[] = new Object[2];
                fila[0] = rs.getObject(1);
                fila[1] = rs.getObject(2);
                model.addRow(fila);
            }
            con.close();
            jTable_categoria.setModel(model);
            jTable_categoria.getColumnModel().getColumn(0).setPreferredWidth(10);
        } catch (SQLException e) {
            System.out.println("Error al cargar la tabla: " + e);
        }
    }

    /**
     * Al seleccionar una fila, este método recupera los detalles completos de
     * la categoría desde MySQL y los coloca en el campo de texto.
     */
    private void EnviarDatosCategoriaSeleccionada(int idCategoria) {
        try {
            Connection con = Conexión.conectar();
            PreparedStatement pst = con.prepareStatement("select * from categoria where id_categoria = ?");
            pst.setInt(1, idCategoria);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                txt_descripcion.setText(rs.getString("descripcion"));
            }
            con.close();
        } catch (SQLException e) {
            System.out.println("Error al seleccionar categoría.");
        }
    }

