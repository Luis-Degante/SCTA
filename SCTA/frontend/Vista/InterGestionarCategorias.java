    /**
     * Procesa la actualización de la descripción de una categoría. Requiere que
     * se haya seleccionado una categoría previamente.
     */
    private void jButton_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_actualizarActionPerformed
        // TODO add your handling code here:
        if (!txt_descripcion.getText().isEmpty()) {

            int confirmacion = JOptionPane.showConfirmDialog(null, "¿Desea actualizar la categoría seleccionada?", "Confirmar Actualización", JOptionPane.YES_NO_OPTION);

            if (confirmacion == JOptionPane.YES_OPTION) {
                categoria categoria = new categoria();
                Ctrl_Categoria controlcategoria = new Ctrl_Categoria();
                categoria.setDescripcion(txt_descripcion.getText().trim());

                if (controlcategoria.actualizar(categoria, idCategoria)) {
                    JOptionPane.showMessageDialog(null, "Categoria actualizada.");
                    txt_descripcion.setText("");
                    this.cargarTablaCategoria();
                } else {
                    JOptionPane.showMessageDialog(null, "Error al actualizar.");
                }
            }

        } else {
            JOptionPane.showMessageDialog(null, "Seleccione una categoria.");
        }

    }//GEN-LAST:event_jButton_actualizarActionPerformed

