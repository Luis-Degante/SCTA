    /**
     * Método: cargarTablaReglas Lógica: Realiza un LEFT JOIN entre 'categoria'
     * y 'regla_descuento'. Si una categoría no tiene regla, asigna valores "0"
     * y "0.0" por defecto.
     */
    private void cargarTablaReglas() {
        Connection cn = Conexión.conectar();
        DefaultTableModel model = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        String sql = "SELECT c.id_categoria, c.descripcion, r.puntos_requeridos, r.porcentaje_descuento "
                + "FROM categoria c "
                + "LEFT JOIN regla_descuento r ON c.id_categoria = r.id_categoria";

        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);

            jTable_productos = new javax.swing.JTable(model);
            jScrollPane1.setViewportView(jTable_productos);

            model.addColumn("ID");
            model.addColumn("Categoría");
            model.addColumn("Puntos Requeridos");
            model.addColumn("% Descuento");

            while (rs.next()) {
                Object fila[] = new Object[4];
                fila[0] = rs.getObject(1);
                fila[1] = rs.getObject(2);
                Object puntos = rs.getObject(3);
                Object porcentaje = rs.getObject(4);
                fila[2] = (puntos != null) ? puntos : "0";
                fila[3] = (porcentaje != null) ? porcentaje : "0.0";
                model.addRow(fila);
            }

            jTable_productos.setSelectionBackground(new java.awt.Color(51, 153, 255));
            jTable_productos.setSelectionForeground(java.awt.Color.WHITE);
            jTable_productos.setRowHeight(30);

            jTable_productos.getTableHeader().setFont(new java.awt.Font("Tahoma", java.awt.Font.BOLD, 12));
            jTable_productos.getTableHeader().setBackground(new java.awt.Color(32, 136, 203));
            jTable_productos.getTableHeader().setPreferredSize(new java.awt.Dimension(0, 35));
            // ------------------------------------------

            jTable_productos.addMouseListener(new java.awt.event.MouseAdapter() {
                @Override
                public void mouseClicked(java.awt.event.MouseEvent evt) {
                    int fila_point = jTable_productos.rowAtPoint(evt.getPoint());
                    if (fila_point > -1) {
                        idCategoriaSeleccionada = (int) model.getValueAt(fila_point, 0);
                        txt_puntosRequeridos.setText(model.getValueAt(fila_point, 2).toString());
                        txt_porcentajeDescuento.setText(model.getValueAt(fila_point, 3).toString());
                    }
                }
            });

            cn.close();
        } catch (SQLException e) {
            System.out.println("Error al llenar la tabla de reglas: " + e);
        }
    }

    private void limpiar() {
        txt_puntosRequeridos.setText("");
        txt_porcentajeDescuento.setText("");
        idCategoriaSeleccionada = 0;
    }

