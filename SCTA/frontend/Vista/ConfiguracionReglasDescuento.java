    /**
     * Evento: Actualizar/Guardar Regla Lógica: Valida la selección de una
     * categoría, verifica que los campos no estén vacíos y envía el objeto
     * ReglaDescuento al controlador para su persistencia.
     */
    private void jButton_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_actualizarActionPerformed
//         TODO add your handling code here:

        ReglaDescuento reglaDescuento = new ReglaDescuento();
        Ctrl_ReglaProducto controlregla = new Ctrl_ReglaProducto();

        if (idCategoriaSeleccionada == 0) {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione una categoría de la tabla.", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (txt_puntosRequeridos.getText().trim().isEmpty() || txt_porcentajeDescuento.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Complete todos los campos.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            int confirmacion = JOptionPane.showConfirmDialog(this, "¿Desea guardar los cambios para esta categoría?", "Confirmar", JOptionPane.YES_NO_OPTION);

            if (confirmacion == JOptionPane.YES_OPTION) {
                try {
                    reglaDescuento.setPuntosRequeridos(Integer.parseInt(txt_puntosRequeridos.getText().trim()));
                    reglaDescuento.setPorcentajeDescuento(Double.parseDouble(txt_porcentajeDescuento.getText().trim()));

                    if (controlregla.actualizarReglas(reglaDescuento, idCategoriaSeleccionada)) {
                        JOptionPane.showMessageDialog(this, "Regla Guardada/Actualizada.", "Éxito", JOptionPane.INFORMATION_MESSAGE);

                        this.cargarTablaReglas();
                        this.limpiar();
                    } else {
                        JOptionPane.showMessageDialog(this, "Error al procesar en la base de datos.", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "Error: Ingrese valores numéricos válidos.", "Error de formato", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_jButton_actualizarActionPerformed

